const myAge = 17
const myHeight = 170

if(myAge>=17 && myHeight>150){
    console.log('Saya bisa punya sim')
}else{
    console.log('saya tidak bisa punya sim')
}

// ternary operator
// syntax: kondisi ? do something : do something else
myAge>=17 && myHeight>150 ? console.log('Saya bisa punya sim') : console.log('saya tidak bisa punya sim')